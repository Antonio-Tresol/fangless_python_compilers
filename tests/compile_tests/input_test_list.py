print("Testing list with strings")
# ['h', 'e', 'l', 'l', 'o']
print(list("hello"))
# ['p', 'y', 't', 'h', 'o', 'n']
print(list("python"))
print("")

print("Testing list with tuples")
# [1, 2, 3]
print(list((1, 2, 3)))
# ['a', 'b', 'c']
print(list(('a', 'b', 'c')))
print("")

print("Testing list with range")
# [0, 1, 2, 3]
print(list(range(4)))
# [10, 11, 12]
print(list(range(10, 13)))
print("")

print("Testing empty list")
# []
print(list())
print("")
