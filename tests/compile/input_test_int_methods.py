# conjugate
print("conjugate examples:")
print((5).conjugate())  # Outputs: 5 (complex conjugate of 5 is itself)
print((-10).conjugate())  # Outputs: -10 (complex conjugate of -10 is itself)

# denominator
print("\ndenominator examples:")
print((5).denominator())  # Outputs: 1 (denominator of any integer is always 1)
print((0).denominator())  # Outputs: 1 (denominator of zero is also 1)

# imag
print("\nimag examples:")
print((5).imag())  # Outputs: 0 (imaginary part of an integer is always 0)
print((0).imag())  # Outputs: 0 (imaginary part of zero is also 0)

# numerator
print("\nnumerator examples:")
print((5).numerator())  # Outputs: 5 (numerator of an integer is the integer itself)
print((-10).numerator())  # Outputs: -10 (numerator of -10 is -10)

# real
print("\nreal examples:")
print((5).real())  # Outputs: 5 (real part of the integer is itself)
print((-10).real())  # Outputs: -10 (real part of -10 is -10)
