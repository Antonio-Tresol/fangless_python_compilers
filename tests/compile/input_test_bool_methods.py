# real
print("real examples:")
print(True.real())   # Outputs: 1 (real part of True, equivalent to 1)
print(False.real())  # Outputs: 0 (real part of False, equivalent to 0)

# imag
print("\nimag examples:")
print(True.imag())   # Outputs: 0 (imaginary part of True, which is always 0)
print(False.imag())  # Outputs: 0 (imaginary part of False, which is always 0)

# conjugate
print("\nconjugate examples:")
print(True.conjugate())   # Outputs: 1 (conjugate of True, equivalent to 1)
print(False.conjugate())  # Outputs: 0 (conjugate of False, equivalent to 0)
